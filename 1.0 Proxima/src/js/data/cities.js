let _byTz = null;
export function cityForTimeZone(tz) {
  if (!_byTz) {
    _byTz = new Map();
    for (const c of CITIES) if (!_byTz.has(c[4])) _byTz.set(c[4], c);
  }
  return _byTz.get(tz) || null;
}

export function searchCities(query, limit = 8) {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  const out = [];
  for (const c of CITIES) {
    if (c[0].toLowerCase().includes(q)) {
      out.push({ name: c[0], cc: c[1], lat: c[2], lon: c[3], tz: c[4] });
      if (out.length >= limit) break;
    }
  }
  return out;
}